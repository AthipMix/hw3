//package Test;
public class TestVariable {
    public static void main(String[] args) {
        // 1) data type char
        char clothingSize = 's'; 
        System.out.println("clothingSize = "+clothingSize);

        // 2) data type int
        int websiteVisitors = 0; 
        System.out.println("websiteVisitors = "+websiteVisitors);

        // 3) data type char
        char Studentgrade = 'A';
        System.out.println("Studentgrade = "+Studentgrade);

        // 4) data type float
        float GPA = 4.00f;
        System.out.println("GPA = "+GPA);

        // 5) data type float
        float PICost = 3.14159f;
        System.out.println("PICost = "+PICost);

        // 6) data type int
        int Expenses = 12000;
        System.out.println("Expenses = "+Expenses);

        // 7) data type int
        int month = 12;
        System.out.println("month = "+month);
    }
}